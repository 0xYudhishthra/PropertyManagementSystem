    private void confirmActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_confirmActionPerformed
        //check that all the fields are filled
        if(descriptionInput.getText().toString().equals("")) {
            JOptionPane.showMessageDialog(null, "Please fill in all the fields!");
            return;
        }

        HashMap<String, String> details = new HashMap<String, String>();
        details.put("RESIDENT ID", residentID);
        details.put("DATE OF COMPLAINT", dateOfComplaint);
        details.put("DESCRIPTION", descriptionInput.getText());
        details.put("COMPLAINT NUMBER", complaintNumber);
        details.put("STATUS", status.toString());

        //update the database
        boolean isBookingUpdated = residentFileHandler.updateComplaint(residentID, details);

        if(isBookingUpdated) {
            JOptionPane.showMessageDialog(null, "Complaint updated successfully!");
            //dispose the current window
            new manageComplaints(residentID).setVisible(true);
            this.dispose();
        }
    }//GEN-LAST:event_confirmActionPerformed

