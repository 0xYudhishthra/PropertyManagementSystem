    private void updateTableList() {
        FileHandler fileHandler = new FileHandler();
        ArrayList<String> data = new ArrayList<String>();
        try {
            data = fileHandler.readData("groups", "BuildingManager");
        } catch (Exception e) {
            // TODO Auto-generated catch block
            e.printStackTrace();
        }

        ArrayList<ArrayList<String>> userArray = new ArrayList<ArrayList<String>>();

        for( int i = 0; i < data.size(); i++ ) {
            String[] temp = data.get(i).split("\\|");
            ArrayList<String> tempArray = new ArrayList<String>();
            for( int j = 0; j < temp.length; j++ ) {
                tempArray.add(temp[j].strip());
            }
            userArray.add(tempArray);
        }

        //add complaintarray into jtable1
        DefaultTableModel model = (DefaultTableModel) jTable2.getModel();
        model.setRowCount(0);
        for( int i = 0; i < userArray.size(); i++ ) {
            model.addRow(new Object[] { userArray.get(i).get(0), userArray.get(i).get(1), userArray.get(i).get(2) });
        }
    }

    private Boolean isEditMode = false;
    private int selectedIndex = 0;

    private void jTable2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:.
        int index = jTable2.getSelectedRow();
        
        isEditMode = true;
        selectedIndex = index;

        TableModel model = jTable2.getModel();

        String employeeId = model.getValueAt(index, 0).toString();
        String employeeName = model.getValueAt(index, 1).toString();
        String groupName = model.getValueAt(index, 2).toString();

        jTextField1.setText(employeeId);
        jTextField2.setText(employeeName);
        jTextField3.setText(groupName);
    }//GEN-LAST:event_jTable1MouseClicked

    private void userClickedSave() {
        String employeeId = jTextField1.getText();
        String employeeName = jTextField2.getText();
        String groupName = jTextField3.getText();

        if( employeeId.isEmpty() || employeeName.isEmpty() || groupName.isEmpty() ) {
            JOptionPane.showMessageDialog(null, "Please fill in all the fields");
            return;
        }

        if( isEditMode ) {
            updateData();
        } else {
            createData();
        }

        try {
            updateTableList();
        } catch (Exception e) {
            // TODO Auto-generated catch block
            e.printStackTrace();
        }
    }

    private void createData() {
        FileHandler fileHandler = new FileHandler();
        //new hashmap string, string
        HashMap<String, String> data = new HashMap<String, String>();
        data.put("EMPLOYEE ID", jTextField1.getText());
        data.put("EMPLOYEE NAME", jTextField2.getText());
        data.put("GROUP NAME", jTextField3.getText());

        try {
            fileHandler.createData("groups", data, "BuildingManager");
        } catch (Exception e) {
            // TODO Auto-generated catch block
            e.printStackTrace();
        }
    }

    private void updateData() {
        FileHandler fileHandler = new FileHandler();
        HashMap<Integer, HashMap<String, String>> data = new HashMap<Integer, HashMap<String, String>>();
        HashMap<String, String> temp = new HashMap<String, String>();
        temp.put("EMPLOYEE ID", jTextField1.getText());
        temp.put("EMPLOYEE NAME", jTextField2.getText());
        temp.put("GROUP NAME", jTextField3.getText());
        data.put(selectedIndex, temp);

        try {
            fileHandler.updateData("groups", data, "BuildingManager");
        } catch (Exception e) {
            // TODO Auto-generated catch block
            e.printStackTrace();
        }
    }

